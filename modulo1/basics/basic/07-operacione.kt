fun main() {
  println("Operadores de Comparación")
  
  val numero1=15
  val numero2= 10
  
  println("$numero1==$numero2=${numero1==numero2}")
  println("$numero1.equals $numero2=${numero1.equals(numero2)}")
  println("$numero1!=$numero2=${numero1!=numero2}")
  println("$numero1>$numero2=${numero1>numero2}")
  println("$numero1>=$numero2=${numero1>=numero2}")
  println("$numero1<$numero2=${numero1<numero2}")
  println("$numero1<=$numero2=${numero1<=numero2}")
  
}